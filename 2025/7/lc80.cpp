#include <iostream>
#include <vector>

using namespace std;

class Solution {
public:
  int removeDuplicates(vector<int> &nums) {
    int size{}, repeat{}, p{1};
    while (size + p < nums.size()) {
      if (nums[size] == nums[size + p]) {
        repeat++;
        if (repeat < 2) {
          swap(nums[size + 1], nums[size + p]);
          size++;
        } else {
          p++;
        }
      } else {
        repeat = 0;
        swap(nums[size + 1], nums[size + p]);
        size++;
      }
      for (auto &n : nums) {
        std::cout << n << ' ';
      }
      std::cout << '\n';
    }
    return size + 1;
  }
};

